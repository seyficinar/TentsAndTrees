
public class Coordinates {
	
	//Coordinates for possible tents
	int x;
	public Coordinates(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	int y;
	
	//Getters and Setters
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
	
	

}
